    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        DictionaryBean db = new DictionaryBean();
        
        db.setBanglaWord(txtBanglaWord.getText().trim());
        db.setEnglishWord(txtEnglishWord.getText().trim());
        db.setPartsOfSpeech(cmbPartsOfSpeech.getSelectedItem().toString());
        
             if(isSumitTrue(db) == true)
             {
                if(prnt.isInDictionary(db.getEnglishWord()) == -1)
                {
                     excon = new ExcelManager();
                     excon.saveData(db, -1);
                    setDictionary();
                    addMoreData();
                }
                else 
                     JOptionPane.showMessageDialog(null, "Word already exists.\nYou can update this word using Update menu.","Error",JOptionPane.ERROR_MESSAGE);
             }
    }//GEN-LAST:event_btnSaveActionPerformed

