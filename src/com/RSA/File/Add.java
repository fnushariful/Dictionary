    private void addMoreData()
    {
        int response = JOptionPane.showConfirmDialog(null, "Do you want to add another word???", "Add Word", JOptionPane.YES_NO_OPTION);
         
          if(response == JOptionPane.YES_OPTION)
          {
              txtBanglaWord.setText(null);
              txtEnglishWord.setText(null);
              cmbPartsOfSpeech.setSelectedIndex(0);
          }
          else 
              this.dispose();
    
    
    }
    
    private void setDictionary()
    {   
        if(tp == false)
            prnt.setE2BDictionary();
        else 
            prnt.setB2EDictionary();
        
        prnt.setVisible(true);
    }
    
    private boolean isSumitTrue(DictionaryBean db)
    {
        if(db.getEnglishWord().length() == 0)
        {
            JOptionPane.showMessageDialog(null, "Enter a English word", "Error",JOptionPane.ERROR_MESSAGE);
            return false;
        }
        if(db.getBanglaword().length() ==0)
        {
            JOptionPane.showMessageDialog(null, "Enter a Bangla word", "Error",JOptionPane.ERROR_MESSAGE);
            return false;
        }
            
        
        
        return true;
    }
    
